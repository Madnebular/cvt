		v |= BAYER_MIX2( BAYER_RGGB_B1( *src1 ), BAYER_RGGB_B1( *src3 ) ); // BLUE
		*dst++ = v;
		v = 0xff000000;
		v |= BAYER_RGGB_R2( *src2 ) << 16; // RED
		v |= BAYER_MIX4( BAYER_RGGB_EVEN_G2( *src1 ), BAYER_RGGB_EVEN_G2( *src3 ), BAYER_RGGB_ODD_G1( *src2 ), BAYER_RGGB_ODD_G2( *src2 ) ) << 8; // GREEN
