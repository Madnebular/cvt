	void SIMD::ConvolveClampSet1f( float* dst, float const* src, const size_t width, float const* weights, const size_t wn ) const
	{
		float const* wp;
		float const* sp;
		float tmp;
		size_t i, k, b1, b2;

		if( wn == 1 ) {
			Mul( dst, src, *weights, width );
			return;
		}

		b1 = ( wn - ( 1 - ( wn & 1 ) ) ) / 2;
		b2 = ( wn + ( 1 - ( wn & 1 ) ) ) / 2;

		/* border 1 */
		i = b1;
		while( i-- ) {
			wp = weights;
			sp = src;
			tmp = *sp * *wp++;
			k = i;
			while( k-- )
				tmp += *sp * *wp++;
			k = wn - 1 - i;
			while( k-- ) {
				tmp += *sp++ * *wp++;
			}
			*dst++ = tmp;
		}


		/* center */
		i = width - wn + 1;
		while( i-- ) {
			k = wn;
			sp = src;
			wp = weights;
			tmp = *sp++ * *wp++;
			k--;
			while( k-- )
				tmp += *sp++ * *wp++;
			*dst++ = tmp;
			src++;
		}

		/* border 2 */
		i = b2;
		while( i-- ) {
			wp = weights;
			sp = src;
			tmp = *sp++ * *wp++;
			k = b1 + i;
			while( k-- ) {
				tmp += *sp++ * *wp++;
			}
			k = b2 - i;
			sp--;
			while( k-- )
				tmp += *sp * *wp++;
			*dst++ = tmp;
			src++;
		}
	}

